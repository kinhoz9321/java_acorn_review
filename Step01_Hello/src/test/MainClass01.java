package test;

//MainClass 라는 이름의 Class 생성
public class MainClass01 {
	
	//main 이라는 이름의 static 메소드 생성
	public static void main(String[] args) {
		
		//syso : 콘솔창에 "Hello, world!" 출력
		System.out.println("Hello, World!");
		
	}
}
